function obj_sort_asc(a,b){
    if (a.priceMin < b.priceMin) {
        return -1;
    } else if (a.priceMin > b.priceMin){
        return 1;
    } else{
        return 0;
    }
}
function obj_sort_dsc(a,b){
    if(a.priceMin < b.priceMin) {
        return 1;
    } else if (a.priceMin > b.priceMin){
        return -1;
    } else{
        return 0;
    }
}
